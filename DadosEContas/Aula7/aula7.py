#  Operadores Aritiméticos
#  x + y    adição              5 + 2 == 7
#  x - y    subtração           5 - 2 == 3
#  x * y    multiplicação       5 * 2 == 10
#  x / y    divisão             5 / 2 == 2.5
#  x ** y   potenciação         5 ** 2 == 25
#  x // y   divisão inteira     5 // 2 == 2
#  x % y    resto da divisão    5 % 2 == 1
#  x = y    atribuição
#  x == y   igual

#  Expressões Ordem de Resolução
#  1 - ()
#  2 - **
#  3 - *, /, //, % (da esquerda para direita)
#  4 - +, - (da esquera para direira)

nome = input('Qual o seu nome? ')
print('Prazer em te conhecer, {:20}!'.format(nome))

# {:20} coloca a variável nome no espaço de 20 caracteres
# Então você pode alinhar essa variável nesse espaço, usando:
# {:>20} para alinhar o texto o mais para direita desse espaço
# {:<20} para alinhar o texto o mais para esquerda desse espaço
# {:^20} para alinhar o texto bem no centro desse espaço
# {:=^20} para alinhar o texto no centro, com os espaços livres sendo substituidos por =




